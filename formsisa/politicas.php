                <br>
                1. Informações coletadas:<br>
                <br>
                Ao utilizar o site da LakerShow ou ao fazer compras conosco, podemos coletar as seguintes informações pessoals:<br>
                <br>
                Nome completo<br>
                <br>
                -Endereço de e-mail<br>
                <br>
                -Endereço de entrega<br>
                <br>
                -Número de telefone<br>
                <br>
                -Informações de pagamento (somente para fins de processamento de pedidos) -Informações de preferência de produtos e serviços<br>
                <br>
                2. Uso das informações:<br>
                <br>
                As Informações pessoals fornecidas pelos nossos clientes são utilizadas para os seguintes propósitos:<br>
                <br>
                Fornecer informações sobre produtos, promoções e ofertas especials Responder a dúvidas e solicitações de suporte<br>
                <br>
                Processar e emdar pedidos de compra Manter uma conta de cliente<br>
                <br>
                Personalizar a experiência do usuário no nosso site<br>
                <br>
                Melhorar nossos produtos e serviços com base no feedback do cliente Realizar pesquisas de mercado e análises estatisticas<br>
                <br>
                3. Armazenamento e Proteção:<br>
                <br>
                Tomamos medidas razoávels para proteger as informações pessoals dos nossos clientes contra acesso não autorizado, alteração, divulgação ou destruição. As Informações de pagamento são criptografadas usando tecnologia SSL (Secure Socket Layer) para garantir a segurança das transações. No entanto, é importante lembrar que nenhum método de transmissão de dados pela internet ou método de armazenamento eletrônico é 100% seguro, e não podemos garantir a segurança absoluta das informações fornecidas pelos clientes.<br>
                <br>
                4. Compartilhamento de informações:<br>
                <br>
                A LakerShow não vende, aluga ou compartilha informações pessoals dos clientes com terceiros para fins comercials, exceto quando necessário para processar pedidos ou cumprir obrigações legais. Podemos compartilhar informações com prestadores de serviços de confiança que nos auxiliam na operação do nosso negócio, como empresas de entrega, processamento de pagamentos e análises de dados. Esses parceiros estão comprometidos em proteger a privacidade dos nossos clientes e não podem utilizar as informações para outros fins.<br>
                <br>
                5. Cookies:<br>
                <br>
                Utilizamos cookies para melhorar a experiência do usuário no nosso site. Os cookies são pequenos arquivos de texto armazenados no navegador do usuário que permitem que o site se lembre de informações especificas sobre a visita do usuário, como preferéncias e itens adicionados ao carrinho. Os cookies não contém informações pessoals identificáveis e podem ser desativados nas configurações do navegador do usuário. No entanto, desativar os cookies pode afetar a funcionalidade do site.<br>
                <br>
                6. Links para sites de terceiros:<br>
                <br>
                Nosso site pode conter links para sites de terceiros, como parceiros comercials ou redes sociais. Esta Politica de Privacidade se aplica apenas ao nosso site, portanto, recomendamos que você re as politicas<br>
